#include <unistd.h>

void    ft_putchar(char c)
{
    write(1, &c, 1);
}

int main(int ac, char **av)
{
    if (ac == 2)
    {
        if (*av[1] != '\0' && *(av[1] + 1) == '\0')
            ft_putchar(*av[1]);
    }
    write(1, "\n", 1);
    return (0);
}